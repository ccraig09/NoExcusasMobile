class mixedCategory {
  constructor(
    id,
    title,
    image,
    subtitle,
    time,
    difficulty,
    logo,
    price,
    description,
    videoURL
  ) {
    this.id = id;
    this.title = title;
    this.image = image;
    this.subtitle = subtitle;
    this.time = time;
    this.difficulty = difficulty;
    this.logo = logo;
    this.price = price;
    this.description = description;
    this.videoURL = videoURL;
  }
}

export default mixedCategory;
